package CWHbasics;

public class NO6IncrementAndDecrementOperators {
    public static void main(String[] args) {
        int i = 56;
        int j = 100;
        System.out.println(i++);  //first i is printed  then i is incremented, so we get o/p as 56.
        System.out.println(i);    // since i was incremented so now value of i is 57
        System.out.println(++j);  //first j is incremented then j is printed
        System.out.println(j);
    }
}
