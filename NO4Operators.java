package CWHbasics;

public class NO4Operators {
    public static void main(String[] args) {
        int a = 13;
        int b = 14;
        int sum = a+b;                 // = is assignment operator
        System.out.println(sum);
        System.out.println(8==9);  //  == is comparison operator  other are >,<, >=, <=
        System.out.println(45>3 && 4>2);   //   logical operators = &&, ||, !
        System.out.println(45>3 || 4<2);   // logical OR ek bhi true to output true

//        Operators in java are
//        1. Arithmetic operator  +,-,*,/,%,++,--
//        2. Assignment operator  =, +=,-=
//        3. Comparison operator  ==, <,> <=, >=
//        4. Logical operator      &&, ||, !
//        5. Bitwise operator      &, |


    }
}
